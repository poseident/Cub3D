#include "../include/cub3d.h"

int	check_color(int r, int g, int b)
{
	if (r < 0  || r > 255)
		return (0);
	if (g < 0  || g > 255)
		return (0);
	if (b < 0  || b > 255)
		return (0);
	return (1);
}
